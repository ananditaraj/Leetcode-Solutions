class Solution {
public:
    long long countCommas(long long n) {
        long long s=0;
        if(n<=999)
            s+=0;
        else if(n<=999999)
            s+=(n-999)*1;
        else if(n<=999999999)
            s+=(n-999999)*2 +(999999-999);
        else if(n<=999999999999)
            s+=(n-999999999)*3+(999999999-999999)*2+(999999-999);
        else if(n<=999999999999999)
            s+=(n-999999999999)*4+(999999999999-999999999)*3+(999999999-999999)*2+(999999-999);
        else
            s+=(n-999999999999)*4+(999999999999-999999999)*3+(999999999-999999)*2+(999999-999)+1;
        return s;       
    }
};